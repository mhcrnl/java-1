/**
 * OneToHundredクラスです。
 */
public class OneToHundred {
        /**
         * mainメソッドです。
         * 
         * @param args コマンドライン引数（未使用）
         */
        public static void main(String[] args) {
                for (int i = 0; i < 101; ++i) {
                        System.out.print(i + " ");
                }
                System.out.println();
        }
}
